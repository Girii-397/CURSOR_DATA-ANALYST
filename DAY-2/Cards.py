# Calculate probability of drawing a King from a deck of cards

# Total number of kings in a deck
num_kings = 4

# Total number of cards in a deck 
total_cards = 52

# Calculate probability as a fraction
probability = f"{num_kings}/{total_cards}"

print(f"The probability of drawing a King is: {probability}")
# This will output: The probability of drawing a King is: 4/52
# Which can be simplified to 1/13

